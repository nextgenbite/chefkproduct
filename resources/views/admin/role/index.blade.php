<script>
    document.addEventListener('DOMContentLoaded', function () {
   // Function to handle "Select All" checkbox
   function handleSelectAllChange() {
        const allGroupCheckboxes = document.querySelectorAll('.group-checkbox');
        const allPermissionCheckboxes = document.querySelectorAll('.permission-checkbox');
        allGroupCheckboxes.forEach(function (checkbox) {
            checkbox.checked = this.checked;
        }, this);
        allPermissionCheckboxes.forEach(function (checkbox) {
            checkbox.checked = this.checked;
        }, this);
    }

    // Event listener for "Select All" checkbox
    document.addEventListener('change', function (event) {
        if (event.target && event.target.id === 'checkbox-all') {
            handleSelectAllChange.call(event.target);
        }
    });
    // Use event delegation for group checkboxes
    document.addEventListener('change', function (event) {
        if (event.target.classList.contains('group-checkbox')) {
            const groupCheckbox = event.target;
            const group = groupCheckbox.id.replace('checkbox-', '');
            const checkboxes = document.querySelectorAll(`.permission-checkbox[data-group="${group}"]`);
            checkboxes.forEach(function (checkbox) {
                checkbox.checked = groupCheckbox.checked;
            });
        }
    });
});

